const ResourceTile = ({ resource, isActive, onClick }) => {
  return (
    <button
      className={`resource-tile ${isActive ? 'active' : ''}`}
      onClick={onClick}
      aria-pressed={isActive}
      aria-label={resource.label}
    >
      <div className="resource-icon">{resource.icon}</div>
      <span className="resource-label">{resource.label}</span>
    </button>
  );
};

const SidebarResourcesGrid = ({ activeResource, onResourceClick, isCollapsed = false }) => {
  if (isCollapsed) {
    return (
      <div className="resources-grid collapsed">
        {resources.map(resource => (
          <button
            key={resource.id}
            className={`resource-tile-icon-only ${activeResource === resource.id ? 'active' : ''}`}
            onClick={() => onResourceClick(resource.id)}
            title={resource.label}
            aria-label={resource.label}
            aria-pressed={activeResource === resource.id}
          >
            {resource.icon}
          </button>
        ))}
      </div>
    );
  }

  return (
    <div className="resources-grid">
      {resources.map(resource => (
        <ResourceTile
          key={resource.id}
          resource={resource}
          isActive={activeResource === resource.id}
          onClick={() => onResourceClick(resource.id)}
        />
      ))}
    </div>
  );
};

export default SidebarResourcesGrid;
